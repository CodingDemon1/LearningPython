int_var = 10
print(type(int_var))
print(int_var)

# Float
float_var = 10.5
print(type(float_var))
print(float_var)

# String
string_var = "Hello"
print(type(string_var))
print(string_var)


# Boolean
bool_var = True
print(type(bool_var))
print(bool_var)

# List
list_var = [1, 2, 3, 4, 5]
print(type(list_var))
print(list_var)

# Tuple
tuple_var = (1, 2, 3, 4, 5)
print(type(tuple_var))
print(tuple_var)


# Dictionary
